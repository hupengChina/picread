package com.ocr.picread.entity;

public class ImgInfo {

    //图片x坐标
    private int x;
    //图片y坐标
    private int y;
    //图片宽度
    private int w;
    //图片长度
    private int h;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }
}
